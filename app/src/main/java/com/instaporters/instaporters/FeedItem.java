public class FeedItem {
    String title;
    String detail;

    public void setTitle(String title) {
        this.title = title;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getTitle() {

        return title;
    }

    public String getDetail() {
        return detail;
    }
}
